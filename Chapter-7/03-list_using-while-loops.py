l = [1, "Ron", False, "This", "Horror", "Ghost"]

i = 0

while (i < len(l)):
    print(l[i])
    i += 1
# We initialize a variable i to 0, which will be used as an index to access the elements of the list l
# The while loop continues to execute as long as the condition (i < len(l)) is true, which means it will run until i is equal to the length of the list     

# Inside the loop, we print the element at index i (l[i]) and then increment i by 1 (i += 1) to move to the next index
# This will print each element of the list l on a new line until we have printed all the elements in the list.
