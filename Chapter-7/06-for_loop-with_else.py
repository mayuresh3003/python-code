l = [1, 7, 8]

for item in l:
    print(item)
else:
    print("done")
# In this code snippet, we have a list l containing several integers. We use a for loop to iterate over each element in the list.
# The variable item takes on the value of each element in the list l during each iteration of the loop. After the loop has finished iterating over all the elements in the list, the else block is executed, which prints "done". This means that after we have printed all the elements of the list, we will see "done" printed on a new line.
